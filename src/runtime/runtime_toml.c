    fputs("static char *cct_rt_toml_stringify(long long doc_handle) {\n", out);
    fputs("    cct_rt_toml_doc_t *doc = (cct_rt_toml_doc_t*)(intptr_t)doc_handle;\n", out);
    fputs("    size_t cap = 512u;\n", out);
    fputs("    size_t len = 0u;\n", out);
    fputs("    char *out_s = (char*)cct_rt_alloc_or_fail(cap);\n", out);
    fputs("    out_s[0] = '\\0';\n", out);
    fputs("    const char *active_prefix = (doc && doc->prefix) ? doc->prefix : \"\";\n", out);
    fputs("    size_t active_prefix_len = strlen(active_prefix);\n", out);
    fputs("    char *current_table = cct_rt_toml_dup(\"__root__\");\n", out);
    fputs("    for (size_t i = 0; doc && doc->storage && i < doc->storage->len; i++) {\n", out);
    fputs("        cct_rt_toml_entry_t *entry = &doc->storage->entries[i];\n", out);
    fputs("        if (active_prefix_len > 0u) {\n", out);
    fputs("            if (strncmp(entry->key, active_prefix, active_prefix_len) != 0) continue;\n", out);
    fputs("            if (entry->key[active_prefix_len] != '\\0' && entry->key[active_prefix_len] != '.') continue;\n", out);
    fputs("        }\n", out);
    fputs("        const char *relative = entry->key + active_prefix_len;\n", out);
    fputs("        if (*relative == '.') relative++;\n", out);
    fputs("        const char *last_dot = strrchr(relative, '.');\n", out);
    fputs("        char *table = NULL;\n", out);
    fputs("        const char *leaf = relative;\n", out);
    fputs("        if (last_dot) {\n", out);
    fputs("            table = cct_rt_toml_dup_n(relative, (size_t)(last_dot - relative));\n", out);
    fputs("            leaf = last_dot + 1;\n", out);
    fputs("        } else {\n", out);
    fputs("            table = cct_rt_toml_dup(\"\");\n", out);
    fputs("        }\n", out);
    fputs("        if (strcmp(table, current_table) != 0) {\n", out);
    fputs("            current_table = table;\n", out);
