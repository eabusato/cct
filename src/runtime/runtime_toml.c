    fputs("                    cct_rt_toml_set_error(\"toml linha invalida\");\n", out);
    fputs("                    return 0LL;\n", out);
    fputs("                }\n", out);
    fputs("                if (strncmp(value, \"\\\"\\\"\\\"\", 3) == 0) {\n", out);
    fputs("                    char *builder = cct_rt_toml_dup(value);\n", out);
    fputs("                    while (1) {\n", out);
    fputs("                        char *close = strstr(builder + 3, \"\\\"\\\"\\\"\");\n", out);
    fputs("                        if (close) break;\n", out);
    fputs("                        if (!line_end) { cct_rt_toml_set_error(\"toml string multiline sem fechamento\"); return 0LL; }\n", out);
    fputs("                        cursor = line_end + 1u;\n", out);
    fputs("                        line_end = strchr(cursor, '\\n');\n", out);
    fputs("                        line_len = line_end ? (size_t)(line_end - cursor) : strlen(cursor);\n", out);
    fputs("                        char *extra = cct_rt_toml_dup_n(cursor, line_len);\n", out);
    fputs("                        size_t old_len = strlen(builder);\n", out);
    fputs("                        size_t extra_len = strlen(extra);\n", out);
    fputs("                        builder = (char*)realloc(builder, old_len + 1u + extra_len + 1u);\n", out);
    fputs("                        if (!builder) cct_rt_fail(\"toml multiline realloc failed\");\n", out);
    fputs("                        builder[old_len] = '\\n';\n", out);
    fputs("                        memcpy(builder + old_len + 1u, extra, extra_len + 1u);\n", out);
    fputs("                    }\n", out);
    fputs("                    char *close = strstr(builder + 3, \"\\\"\\\"\\\"\");\n", out);
    fputs("                    char *payload = cct_rt_toml_dup_n(builder + 3, (size_t)(close - (builder + 3)));\n", out);
    fputs("                    value = cct_rt_toml_dup(payload);\n", out);
    fputs("                    char *full_key = cct_rt_toml_join_key(section, key);\n", out);
    fputs("                    cct_rt_toml_set_scalar(storage, full_key, CCT_RT_TOML_STRING, payload);\n", out);
    fputs("                } else {\n", out);
    fputs("                    char *full_key = cct_rt_toml_join_key(section, key);\n", out);
    fputs("                    int array_kind = 0;\n", out);
    fputs("                    char **array_items = NULL;\n", out);
    fputs("                    size_t array_len = 0u;\n", out);
    fputs("                    if (cct_rt_toml_parse_array(value, &array_kind, &array_items, &array_len)) {\n", out);
    fputs("                        cct_rt_toml_set_array(storage, full_key, array_kind, array_items, array_len);\n", out);
    fputs("                    } else {\n", out);
    fputs("                        int scalar_kind = 0;\n", out);
    fputs("                        char *scalar_text = NULL;\n", out);
    fputs("                        if (!cct_rt_toml_parse_scalar_text(value, &scalar_kind, &scalar_text)) return 0LL;\n", out);
    fputs("                        cct_rt_toml_set_scalar(storage, full_key, scalar_kind, scalar_text);\n", out);
    fputs("                    }\n", out);
    fputs("                }\n", out);
    fputs("            }\n", out);
    fputs("        }\n", out);
    fputs("        if (!line_end) break;\n", out);
    fputs("        cursor = line_end + 1u;\n", out);
    fputs("    }\n", out);
    fputs("    return (long long)(intptr_t)doc;\n", out);
    fputs("}\n\n", out);

