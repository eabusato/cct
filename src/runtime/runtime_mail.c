    EMIT("static int cct_rt_mail_auth_xoauth2(cct_rt_mail_conn_t *conn, const char *username, const char *password) {");
    EMIT("    const char *user = username ? username : \"\";");
    EMIT("    const char *token = password ? password : \"\";");
    EMIT("    const char *prefix_a = \"user=\";");
    EMIT("    const char *prefix_b = \"\\x01\" \"auth=Bearer \";");
    EMIT("    const char *suffix = \"\\x01\" \"\\x01\";");
    EMIT("    size_t raw_len = strlen(prefix_a) + strlen(user) + strlen(prefix_b) + strlen(token) + strlen(suffix);");
    EMIT("    unsigned char *raw = (unsigned char*)cct_rt_alloc_or_fail(raw_len + 1U);");
    EMIT("    char payload[2048];");
    EMIT("    char cmd[2300];");
    EMIT("    size_t off = 0;");
    EMIT("    memcpy(raw + off, prefix_a, strlen(prefix_a)); off += strlen(prefix_a);");
    EMIT("    memcpy(raw + off, user, strlen(user)); off += strlen(user);");
    EMIT("    memcpy(raw + off, prefix_b, strlen(prefix_b)); off += strlen(prefix_b);");
    EMIT("    memcpy(raw + off, token, strlen(token)); off += strlen(token);");
    EMIT("    memcpy(raw + off, suffix, strlen(suffix)); off += strlen(suffix);");
    EMIT("    raw[off] = '\\0';");
    EMIT("    if (!cct_rt_mail_base64_raw(raw, off, payload, sizeof(payload))) {");
    EMIT("        cct_rt_free_ptr(raw);");
    EMIT("        cct_rt_mail_set_error(conn, \"falha ao codificar AUTH XOAUTH2\");");
    EMIT("        return 0;");
