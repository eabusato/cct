    EMIT("static char *cct_rt_mail_dotstuff(const char *mime_text) {");
    EMIT("    const char *src = mime_text ? mime_text : \"\";");
    EMIT("    size_t n = strlen(src);");
    EMIT("    size_t cap = n * 2U + 8U;");
    EMIT("    char *out_s = (char*)cct_rt_alloc_or_fail(cap);");
    EMIT("    size_t i = 0;");
    EMIT("    size_t j = 0;");
    EMIT("    int at_line_start = 1;");
    EMIT("    while (i < n) {");
    EMIT("        char ch = src[i];");
    EMIT("        if (at_line_start && ch == '.') out_s[j++] = '.';");
    EMIT("        if (ch == '\\r') {");
    EMIT("            out_s[j++] = '\\r';");
    EMIT("            out_s[j++] = '\\n';");
    EMIT("            if ((i + 1U) < n && src[i + 1U] == '\\n') i++;");
    EMIT("            at_line_start = 1;");
    EMIT("        } else if (ch == '\\n') {");
    EMIT("            out_s[j++] = '\\r';");
    EMIT("            out_s[j++] = '\\n';");
    EMIT("            at_line_start = 1;");
    EMIT("        } else {");
    EMIT("            out_s[j++] = ch;");
    EMIT("            at_line_start = 0;");
    EMIT("        }");
    EMIT("        i++;");
    EMIT("    }");
    EMIT("    if (j < 2U || out_s[j - 2U] != '\\r' || out_s[j - 1U] != '\\n') {");
    EMIT("        out_s[j++] = '\\r';");
    EMIT("        out_s[j++] = '\\n';");
    EMIT("    }");
    EMIT("    out_s[j] = '\\0';");
    EMIT("    return out_s;");
