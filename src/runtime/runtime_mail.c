    EMIT("static int cct_rt_mail_connect_tcp(cct_rt_mail_conn_t *conn, const char *host, long long port) {");
    EMIT("    struct addrinfo hints;");
    EMIT("    struct addrinfo *res = NULL;");
    EMIT("    struct addrinfo *it = NULL;");
    EMIT("    char port_text[32];");
    EMIT("    memset(&hints, 0, sizeof(hints));");
    EMIT("    hints.ai_family = AF_UNSPEC;");
